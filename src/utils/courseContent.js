const getTrack = (category) => TRACKS[category] || TRACKS.default;

export const buildCourseLessons = (course) => {
  const track = getTrack(course.category);
  const lessonCount = Math.max(Number(course.lessons) || 1, 1);

  return Array.from({ length: lessonCount }, (_, index) => {
    const topic = track.topics[index % track.topics.length];
    const lessonNumber = index + 1;

    return {
      id: `${course.id}-lesson-${lessonNumber}`,
      title: `${lessonNumber}. ${topic}`,
      theory: [
        track.theory,
        `В контексте курса «${course.title}» этот урок помогает перейти от общей идеи к конкретному навыку. Сначала разберите понятие, затем найдите пример и только после этого выполняйте практику.`,
      ],
      practice: track.practice,
      quiz: {
        question: track.question,
        options: track.options,
        correctIndex: track.correctIndex,
        explanation:
          'Верно: обучение становится устойчивым, когда теория сразу связывается с понятным действием и проверкой.',
      },
    };
  });
};
